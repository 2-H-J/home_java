
public class CommentMain {

	public static void main(String[] args) {
//		컨트롤 + 스페이스 : 자동완성
		/*
		 * - 주석 코드에 설명문을 작성하는 영역 주석은 실행이 안되는 코드
		 */
//		 한 줄 단위 주석 : (컨트롤 + /) , (컨트롤 + 쉬프트 + c)

		/*
		 * 여러줄 주석 : ( 컨트롤 + 쉬프트 + /)
		 */

//		자동 정렬 : 컨트롤 + 쉬프트 + f

		System.out.println("Hello - 1");
//		System.out.println("Hello - 2");
		System.out.println("Hello - 3");
		/*
		 * System.out.println("Hello - 4");
		 */
		System.out.println("Hello - 6");
		System.out.println("Hello - 6"); // 컨트롤 + 알트 + 방향키 : 복사
										 // 컨트롤 + D : 그 줄 삭제
		System.out.println("Hello - 5"); // 알트 + 위 아래 방향키 : 코드 위치 변경
		
	}

}
