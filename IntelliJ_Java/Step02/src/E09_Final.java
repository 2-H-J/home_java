public class E09_Final {
    public static void main(String[] args) {
        /*
        * 상수 : 한번 저장한 값을 바꾸지 못하게 하는 변수
        *
        * 상수명 지을 때 규칙 : Snake Case(스네이크 케이스)
        *   1. 상수명은 모두 대문자로 구성
        *   2. 합성어 일 경우 단어들 사이에 _로 연결
        *       예 > 최대 학생 인원수 -> STUDENT_MAX_COUNT
        * */

       final double PI = 3.1415; // final 선언으로 상수 선언가능
    }
}
